# 创建一个包含单引号的字符串
String1 = "Welcome to Malya's World"
print("使用单引号创建的字符串：")
print(String1)

# 创建一个包含双引号的字符串
String1 = "I'm a TechGeek"
print("\n使用双引号创建的字符串：")
print(String1)

# 创建一个包含三引号的字符串
String1 = '''I'm Malya and I live in a world of "TechGeeks"'''
print("\n使用三引号创建的字符串：")
print(String1)

# 使用三引号创建的字符串可以包含多行
String1 = """Smile 
             For 
             Life"""
print("\n创建多行字符串：")
print(String1)
